/** Database Pool */
const pool = usePgPool()

/**------+---------+---------+---------+---------+---------+---------+----------
 * Helper
---------+---------+---------+---------+---------+---------+---------+--------*/



/**------+---------+---------+---------+---------+---------+---------+----------
 * Exports
---------+---------+---------+---------+---------+---------+---------+--------*/

/**
 * @returns 
 */
export async function getRoles() {
  const res = await pool.query(`SELECT UR.id, UR.title, UR."comment" FROM sbos.user_roles UR WHERE UR.deleted_at IS NULL`)
  return res.rows ?? []
}
